    private void loadListServer() {
        DefaultTableModel model = new DefaultTableModel(null, ServerColums) {
            @Override
            public boolean isCellEditable(int i, int i1) {
                return false;
            }
        };

        String[] servers = listServer.split(">");
        for (int i = 0; i < servers.length; i++) {
            String[] info = servers[i].split("`");
            String[] files = info[2].split(":");
            Object[] items = new Object[]{i + 1, info[0], info[1], files.length};
            model.addRow(items);
        }
        JTListServer.setModel(model);
        JTListServer.setRowSelectionInterval(0, 0);
    }

    public static String humanReadableByteCountSI(long bytes) {
        if (-1000 < bytes && bytes < 1000) {
            return bytes + " B";
        }
        CharacterIterator ci = new StringCharacterIterator("KMGTPE");
        while (bytes <= -999_950 || bytes >= 999_950) {
            bytes /= 1000;
            ci.next();
        }
        return String.format("%.1f %cB", bytes / 1000.0, ci.current());
    }

