    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        // TODO add your handling code here:
        mainCardLayout.show(pnMainCard, "pnListFile");
        String[] servers = listServer.split(">");
        String[] info = servers[JTListServer.getSelectedRow()].split("`");
        lbIP.setText("IP: " + info[0]);
        lbPort.setText("Port: " + info[1]);
        String[] listFile = info[info.length - 1].split(":");
        lbNumofFile.setText("Số lượng File: " + listFile.length);
        DefaultTableModel model = new DefaultTableModel(null, FileColums) {
            public Class<?> getColumnClass(int column) {
                switch (column) {
                    case 0:
                        return String.class;
                    case 1:
                        return String.class;
                    case 2:
                        return String.class;
                    case 3:
                        return Boolean.class;
                    default:
                        return String.class;
                }
            }

            @Override
            public boolean isCellEditable(int i, int i1) {
                if (i1 == 3) {
                    return true;
                }
                return false;
            }
        };

        listChoosen = new ArrayList();

        for (int i = 0; i < listFile.length; i++) {
            String[] result = listFile[i].split("\t");
            String fileSize = humanReadableByteCountSI(Long.parseLong(result[1]));
            Object[] items = new Object[]{i + 1, result[0], fileSize, false};
            model.addRow(items);

            listChoosen.add(new ShareFile(
                    result[0],
                    Long.valueOf(result[1])
            ));
        }
        JTListFile.setModel(model);

    }//GEN-LAST:event_btnConnectActionPerformed

