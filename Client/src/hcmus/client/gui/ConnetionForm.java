    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        // TODO add your handling code here:
        if (txtIP.getText().compareTo("") == 0 || txtPort.getText().compareTo("") == 0) {
            JOptionPane.showMessageDialog(null, "Vui lòng nhập đầy đủ thông tin kết nối!!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // CHECK_VALID_IP
        if (isValidInet4Address(txtIP.getText())) {
            // hợp lệ
        } else {
            JOptionPane.showMessageDialog(null, "IP không hợp lệ xin vui lòng kiểm tra lại!!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // CHECK_PORT_VALID
        Boolean isValidPort = true;
        int port;
        try {
            port = Integer.parseInt(txtPort.getText());
            if (port < 1 || port > 65353) {
                isValidPort = false;
            }
        } catch (Exception e) {
            isValidPort = false;
        }

        if (!isValidPort) {
            JOptionPane.showMessageDialog(null, "Port không hợp lệ xin vui lòng kiểm tra lại!!!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        this.setVisible(false);

        DevClientForm clientForm = new DevClientForm();
        clientForm.setVisible(true);
        

    }//GEN-LAST:event_btnConnectActionPerformed

